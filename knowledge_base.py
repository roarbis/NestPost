def pick_next_topic(recent_topics: list) -> dict:
    """Pick the topic least recently used from the rotation."""
    recent_ids = []
    for t in recent_topics:
        for tmpl in TOPIC_TEMPLATES:
            if tmpl["topic"].lower() in t.lower():
                recent_ids.append(tmpl["id"])

    for tmpl in TOPIC_TEMPLATES:
        if tmpl["id"] not in recent_ids:
            return tmpl

    return TOPIC_TEMPLATES[0]
